package lesson;

public class Cast {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int c = 4;

		System.out.println("(a + b) / cをint型で表示");
		System.out.println((a + b) / c);

		System.out.println("(a + b) / cをdouble型で表示");
		System.out.println((double)(a + b) / c);



	}

}
